#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;

	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			*(char *)(s + i) = (char)('\0');
			i++;
		}
	}
}
